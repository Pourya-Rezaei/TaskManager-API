namespace TaskManager.Domain.Enums
{
    /// <summary>
    /// وضعیت‌های ممکن برای یک تسک در سیستم مدیریت وظایف.
    /// این enum سه حالت اصلی چرخه حیات یک تسک را تعریف می‌کند:
    /// - Todo: تسک ایجاد شده اما هنوز شروع نشده است.
    /// - InProgress: تسک در حال انجام توسط کاربر است.
    /// - Done: تسک تکمیل شده و به پایان رسیده است.
    /// </summary>
    public enum TaskStatus
    {
        /// <summary>
        /// وضعیت اولیه تسک؛ نشان می‌دهد تسک در صف انتظار برای شروع قرار دارد.
        /// مقدار عددی: 1
        /// </summary>
        Todo = 1,

        /// <summary>
        /// وضعیت میانی تسک؛ نشان می‌دهد کار روی تسک آغاز شده و در جریان است.
        /// مقدار عددی: 2
        /// </summary>
        InProgress = 2,

        /// <summary>
        /// وضعیت نهایی تسک؛ نشان می‌دهد تمام مراحل انجام تسک تکمیل شده است.
        /// مقدار عددی: 3
        /// </summary>
        Done = 3
    }
}
